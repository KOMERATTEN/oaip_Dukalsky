colors = {
    "красный": (255, 0, 0),
    "зелёный": (0, 255, 0),
    "синий": (0, 0, 255),
    "жёлтый": (255, 255, 0),
    "белый": (255, 255, 255),
}
    
red = colors["красный"]
green = colors["зелёный"]
mixed = ((red[0]+green[0]) // 2, 
         (red[1]+green[1]) // 2, 
         (red[2]+green[2]) // 2,)
print(f"красный + зелёный = жёлтый: {mixed}")

white = colors["белый"]
invers = (255 - white[0], 255 - white[1], 255 - white[2])
print(f"Инверсированый белый: {invers}")